package com.eucaliptus.springboot_app_person.enums;

/**
 * Enumeración que representa los diferentes tipos de personas.
 * Esta enumeración es utilizada para clasificar a una persona como natural o jurídica
 * en el sistema.
 */

public enum EnumPersonType {
    /**
     * Persona natural.
     * Representa a un individuo como entidad única.
     */
    NATURAL,
    /**
     * Persona jurídica.
     * Representa a una entidad o empresa registrada legalmente.
     */
    JURIDICA
}
